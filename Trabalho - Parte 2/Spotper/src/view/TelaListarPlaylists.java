    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(Playlists.getSelectedRow()!=-1){
            int cod_play = (int) Playlists.getValueAt(Playlists.getSelectedRow(),0);
            if(albumFaixa==1){
                AlbumController albc = new AlbumController();
                if(albc.inserirPlaylist(cod, cod_play)){
                    JOptionPane.showMessageDialog(null, "Album inserido com sucesso na Playlist!");
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Falha ao inserir Album na Playlist!");
                } 
            }else{
               FaixaController faixac = new FaixaController();
                if(faixac.inserirPlaylist(cod, cod_play,cod_album)){
                    JOptionPane.showMessageDialog(null, "Faixa inserida com sucesso na Playlist!");
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Falha ao inserir Faixa na Playlist!");
                }                
            }
                       
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma playlist!");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

