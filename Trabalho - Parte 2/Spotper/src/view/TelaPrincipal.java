    private void listPlaylist(){
        PlaylistController playc = new PlaylistController();
        
        DefaultTableModel modelo = (DefaultTableModel) Playlist.getModel();
        modelo.setNumRows(0);
                
        for(Playlist p : playc.getPlaylists()){
            modelo.addRow(new Object[]{
                p.getCodPlay(),
                p.getNome(),
                p.getDataCriacao(),
                p.getDuracaoExec()
            });
        }
    }
    
