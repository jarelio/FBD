    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(Playlist.getSelectedRow()!=-1){
            int cod_play = (int) Playlist.getValueAt(Playlist.getSelectedRow(),0);
            PlaylistController playc = new PlaylistController();
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente remover essa playlist?","Confirmar remoção",YES_NO_OPTION);
            if(resposta == JOptionPane.YES_OPTION){
                if(playc.deletePlaylist(cod_play)){
                    JOptionPane.showMessageDialog(null, "Playlist removida com sucesso!");
                    this.listPlaylist();
                }else{
                    JOptionPane.showMessageDialog(null, "Falha ao remover playlist!");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"Selecione uma playlist!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

