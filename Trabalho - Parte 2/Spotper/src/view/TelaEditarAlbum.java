    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AlbumController albumc = new AlbumController();
        DateFormat df = new SimpleDateFormat("MM/dd/yyyy");
        Date data_gravacao = null;
        Date data_compra = null;

        try {
            data_gravacao = (Date) df.parse(dataGravacao.getText());
            data_compra = df.parse(dataCompra.getText());
        } catch (ParseException ex) {
            Logger.getLogger(TelaEditarAlbum.class.getName()).log(Level.SEVERE, null, ex);
        }        
        if(data_compra.before(data_gravacao)){
            JOptionPane.showMessageDialog(null,"Digite uma data de compra maior que a de gravação!");
        }else{
            if(albumc.getGravadora(parseInt(codGrav.getText()))){
                int codalbum = parseInt(codAlbum.getText());
                int cod_grav = parseInt(codGrav.getText());
                String descricao = descricaoAlb.getText();
                float preco_compra = parseFloat(precoCompra.getText());
                String tipo_compra = tipoCompra.getSelectedItem().toString();

                if(albumc.updateAlbum(codalbum,cod_grav,descricao,preco_compra,data_compra,tipo_compra,data_gravacao)){
                    JOptionPane.showMessageDialog(null,"Album atualizado com sucesso!");
                }else{
                    JOptionPane.showMessageDialog(null,"Falha ao atualizar album!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Digite uma gravadora existente!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

