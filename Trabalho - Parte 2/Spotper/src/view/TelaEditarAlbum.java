    private void listAlbum(){
        AlbumController albumc = new AlbumController();
        Album album;
        album = albumc.selectAlbum(cod_album);
       
       jLabel1.setText(album.getDescricao());
       codAlbum.setText(Integer.toString(album.getCodAlbum()));
       codGrav.setText(Integer.toString(album.getCodGrav()));
       descricaoAlb.setText(album.getDescricao());
       precoCompra.setText(Float.toString(album.getPrecoCompra()));
       DateFormat df = new SimpleDateFormat("MM/dd/yyyy");
       String dataGrav = df.format(album.getDataGrav());
       dataGravacao.setText(dataGrav);
        switch (album.getTipoCompra()) {
            case "cd":
                tipoCompra.setSelectedIndex(0);
                break;
            case "vinil":
                tipoCompra.setSelectedIndex(1);
                break;
            default:
                tipoCompra.setSelectedIndex(2);
                break;
        }
       String dataComp = df.format(album.getDataCompra());
       dataCompra.setText(dataComp);
    }
    
    private MaskFormatter setMascara(String mascara){
        MaskFormatter mask = null;
        try{
            mask = new MaskFormatter(mascara);                      
        }catch(java.text.ParseException ex){}
        return mask;
    }
